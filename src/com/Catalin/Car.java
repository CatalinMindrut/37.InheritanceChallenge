package com.Catalin;

public class Car {
    private int wheels;
    private int steeringWheel;
    private int speeds;
    private int windshield;

    public Car(int wheels, int steeringWheel, int speeds, int windshield) {
        this.wheels = wheels;
        this.steeringWheel = steeringWheel;
        this.speeds = speeds;
        this.windshield = windshield;
    }
}
